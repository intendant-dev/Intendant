#[cfg(test)]
mod tests {
    use super::*;

    /// The committed outcomes map matches the generator byte-exactly
    /// (the coverage instance of the vector drift gate).
    #[test]
    fn committed_outcomes_map_matches_generator() {
        let path = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .join("..")
            .join("coverage")
            .join("outcomes-map.json");
        let committed = std::fs::read_to_string(&path)
            .unwrap_or_else(|_| panic!("{} not minted", path.display()));
        assert_eq!(
            committed,
            outcomes_map_file(),
            "outcomes-map.json drifted from the builders"
        );
    }

    /// The shrink-only pin: the derived uncovered set equals the
    /// declared one exactly, in both directions.
    #[test]
    fn uncovered_outcomes_pinned_shrink_only() {
        let map = outcome_map();
        let derived: Vec<&str> = map
            .iter()
            .filter(|(_, v)| v.is_empty())
            .map(|(o, _)| *o)
            .collect();
        let mut pinned: Vec<&str> = UNCOVERED_10_4.to_vec();
        pinned.sort_unstable();
        assert_eq!(
            derived, pinned,
            "uncovered §10.4 outcomes moved: new coverage must delete its pin entry; \
             a regression must be fixed, not re-pinned"
        );
    }

    /// Every harvested outcome string is §10.4 vocabulary (the
    /// harvest panics inline; this pins the invariant as a test).
    #[test]
    fn harvested_outcomes_are_vocabulary() {
        let _ = outcome_map();
    }

    /// The §13.3 obligation ledger: quotes verbatim, line-ranges
    /// covering the WHOLE section, vectors real, statuses coherent.
    #[test]
    fn obligations_ledger_is_sound() {
        let root = std::path::Path::new(env!("CARGO_MANIFEST_DIR")).join("..");
        let spec = std::fs::read_to_string(root.join("owner-plane-d0a-spec.md")).unwrap();
        // The §13.3 slice, exclusive of its heading and terminator.
        let start = spec.find("### 13.3 Families").expect("§13.3 present");
        let end = spec[start..]
            .find("\n## 14.")
            .map(|i| start + i)
            .expect("§14 terminates §13.3");
        let section = &spec[start..end];
        let lines: Vec<&str> = section.lines().collect();

        let ledger: Json = serde_json::from_str(
            &std::fs::read_to_string(root.join("coverage").join("obligations-13-3.json")).unwrap(),
        )
        .unwrap();
        let names: std::collections::BTreeSet<String> =
            all_vectors().into_iter().map(|v| v.name).collect();

        let entries = ledger["obligations"].as_array().expect("obligations");
        assert!(!entries.is_empty());
        let mut covered = vec![false; lines.len()];
        let mut ids = std::collections::BTreeSet::new();
        for e in entries {
            let id = e["id"].as_str().expect("id");
            assert!(ids.insert(id.to_string()), "duplicate obligation id {id}");
            // Verbatim quote pin: the transcription cannot drift
            // from the spec text it claims to cover.
            let quote = e["quote"].as_str().expect("quote");
            assert!(
                section.contains(quote),
                "{id}: quote not verbatim in §13.3: {quote:?}"
            );
            // Line coverage (1-indexed within the section).
            let lr = e["lines"].as_array().expect("lines");
            let (a, b) = (
                lr[0].as_u64().expect("start") as usize,
                lr[1].as_u64().expect("end") as usize,
            );
            assert!(a >= 1 && a <= b && b <= lines.len(), "{id}: line range");
            for c in covered.iter_mut().take(b).skip(a - 1) {
                *c = true;
            }
            // Vector references + status coherence.
            let vs = e["vectors"].as_array().expect("vectors");
            for v in vs {
                let n = v.as_str().expect("vector name");
                assert!(names.contains(n), "{id}: unknown vector {n:?}");
            }
            let status = e["status"].as_str().expect("status");
            match status {
                "vectored" | "partial" => {
                    assert!(!vs.is_empty(), "{id}: {status} with no vectors")
                }
                // pending = unvectored debt; code-test = executed by
                // named Rust unit tests (a real execution surface,
                // unlike surface annotations) with no vector shape;
                // structural = non-obligation section furniture.
                "pending" | "structural" => {
                    assert!(vs.is_empty(), "{id}: {status} yet lists vectors")
                }
                "code-test" => {
                    assert!(vs.is_empty(), "{id}: code-test yet lists vectors");
                    assert!(
                        e["tests"].as_array().is_some_and(|t| !t.is_empty()),
                        "{id}: code-test must name its tests"
                    );
                }
                other => panic!("{id}: unknown status {other:?}"),
            }
        }
        // Completeness: every line of §13.3 is claimed by some
        // obligation (blank lines included — they belong to the
        // obligation whose prose they separate).
        let holes: Vec<usize> = covered
            .iter()
            .enumerate()
            .filter(|(_, c)| !**c)
            .map(|(i, _)| i + 1)
            .collect();
        assert!(
            holes.is_empty(),
            "§13.3 lines unclaimed by any obligation: {holes:?}"
        );
        // The executed-surface honesty rider.
        assert_eq!(
            ledger["executed_surfaces"],
            json!(["rust-core", "rust-reducer"]),
            "the ledger must state the honest executed surfaces"
        );
        // The D-203 Gate-B deferral record must exist and must list
        // EXACTLY the uncovered §10.4 outcomes (deferral is a
        // decision, not drift).
        let def = &ledger["gate_b_deferrals"];
        let listed: Vec<&str> = def["outcomes"]
            .as_array()
            .expect("gate_b_deferrals.outcomes")
            .iter()
            .map(|v| v.as_str().expect("outcome name"))
            .collect();
        assert_eq!(
            listed,
            UNCOVERED_10_4.to_vec(),
            "the deferral record must match the uncovered set exactly"
        );
        assert!(
            def["sagas"].as_array().is_some_and(|a| !a.is_empty()),
            "the deferred ceremony sagas must be named"
        );
    }
}
