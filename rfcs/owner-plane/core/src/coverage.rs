//! Coverage inventory (the repair tranche's machine-enforced map):
//! the §10.4 outcome → vector map is GENERATED from the builders
//! (harvested off every vector's `expected` tree) and committed as
//! `coverage/outcomes-map.json` (drift-gated like the vectors), and
//! the uncovered set is pinned SHRINK-ONLY — covering an outcome
//! requires deleting its pin entry, and losing coverage of a covered
//! outcome fails the suite. A vector's `surfaces` array is an
//! ANNOTATION (declared applicability), never execution: the only
//! surfaces that EXECUTE this corpus today are the two Rust
//! reference implementations, and the map says so explicitly.
//!
//! The §13.3 obligation ledger is the hand-transcribed sibling
//! (`coverage/obligations-13-3.json`), enforced by
//! `obligations_ledger_is_sound`: every entry's `quote` appears
//! verbatim inside §13.3, entry line-ranges COVER the whole section
//! (completeness — a spec edit that adds an obligation breaks the
//! coverage), every named vector exists, and status is consistent
//! with the vector list.

use crate::outcomes::OUTCOMES;
use crate::vector::Vector;
use serde_json::{json, Value as Json};
use std::collections::BTreeMap;

/// Every minted vector, one list (the mint bin's assembly order).
pub fn all_vectors() -> Vec<Vector> {
    let mut all = crate::tranche::tranche();
    all.extend(crate::corpus::corpus());
    all.extend(crate::corpus_fold::corpus_fold());
    all.extend(crate::corpus_recovery::corpus_recovery());
    all.extend(crate::corpus_edge::corpus_edge());
    all.extend(crate::corpus_migration::corpus_migration());
    all.extend(crate::corpus_status::corpus_status());
    all.extend(crate::corpus_erase::corpus_erase());
    all.extend(crate::corpus_time::corpus_time());
    all.extend(crate::corpus_ctrl::corpus_ctrl());
    all.extend(crate::corpus_budget::corpus_budget());
    all.extend(crate::corpus_audit::corpus_audit());
    all
}

/// Collect every string under an `"outcome"` key anywhere in the
/// vector's EXPECTED tree (per_item rows, per_record rows, erase
/// outcomes rows, negative arms — inputs are never scanned).
fn harvest(node: &Json, into: &mut Vec<String>) {
    match node {
        Json::Object(m) => {
            if let Some(Json::String(o)) = m.get("outcome") {
                into.push(o.clone());
            }
            for v in m.values() {
                harvest(v, into);
            }
        }
        Json::Array(a) => {
            for v in a {
                harvest(v, into);
            }
        }
        _ => {}
    }
}

/// The §10.4 map: outcome → sorted vector names whose expected tree
/// asserts it.
pub fn outcome_map() -> BTreeMap<&'static str, Vec<String>> {
    let mut map: BTreeMap<&'static str, Vec<String>> =
        OUTCOMES.iter().map(|o| (*o, Vec::new())).collect();
    for v in all_vectors() {
        let mut found = Vec::new();
        harvest(&v.to_json()["expected"], &mut found);
        found.sort();
        found.dedup();
        for o in found {
            let slot = map
                .get_mut(o.as_str())
                .unwrap_or_else(|| panic!("{}: outcome {o:?} outside §10.4", v.name));
            slot.push(v.name.clone());
        }
    }
    for names in map.values_mut() {
        names.sort();
    }
    map
}

/// The committed `coverage/outcomes-map.json` content.
pub fn outcomes_map_file() -> String {
    let map = outcome_map();
    let covered: serde_json::Map<String, Json> = map
        .iter()
        .filter(|(_, v)| !v.is_empty())
        .map(|(o, v)| (o.to_string(), json!(v)))
        .collect();
    let uncovered: Vec<&str> = map
        .iter()
        .filter(|(_, v)| v.is_empty())
        .map(|(o, _)| *o)
        .collect();
    let doc = json!({
        "$comment": "GENERATED by the core mint bin — the §10.4 outcome → vector map, harvested from every vector's expected tree. Edit builders, never this file; the drift gate pins it.",
        "executed_surfaces": ["rust-core", "rust-reducer"],
        "surface_annotation_note": "A vector's `surfaces` array declares §13.2 applicability; it is NOT execution. No browser, native-crypto, or per-OS storage lane has executed this corpus — see execution-lanes-plan.md.",
        "covered": covered,
        "uncovered": uncovered,
    });
    let mut s = serde_json::to_string_pretty(&doc).expect("map serializes");
    s.push('\n');
    s
}

